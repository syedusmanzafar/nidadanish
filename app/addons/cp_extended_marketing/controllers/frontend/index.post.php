use Tygh\Registry;

if (!defined('BOOTSTRAP')) { die('Access denied'); }

if (!empty($_REQUEST['hash'])) {
    $data = fn_cp_em_get_session_data_by_hash($_REQUEST['hash']);
    if (!empty($data) && !empty($data['notice_id']) && empty(Tygh::$app['session']['cp_em_return_from_mail'])) {
        fn_cp_em_update_statistics($data['notice_id'], array('returns_form_email' => 1), '+');
        Tygh::$app['session']['cp_em_return_from_mail'] = $data['notice_id'];
    }
}