use Tygh\Registry;

function fn_settings_variants_addons_cp_live_search_show_items()
{
    $show_items = array();
    $schema = fn_get_schema('cp_live_search', 'show_items', 'php', true);
    if (!empty($schema)) {
        foreach ($schema as $key => $item) {
            $show_items[$key] = !empty($item['title']) ? $item['title'] : __($key);
        }
    }
    return $show_items;
}