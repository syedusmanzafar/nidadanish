use Tygh\Registry;

function fn_exim_get_search_type($type_code)
{
    $types = array(
        'S' => __('simple_search'),
        'L' => __('cp_live_search')
    );
    return !empty($types[$type_code]) ? $types[$type_code] : '';
}

function fn_exim_get_search_item_phrase($search_id)
{
    return db_get_field('SELECT search FROM ?:cp_search_history WHERE search_id = ?i', $search_id);
}